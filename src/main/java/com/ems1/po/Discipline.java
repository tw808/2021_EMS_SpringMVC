package com.ems1.po;

public class Discipline {
    private Integer discId;

    private String discName;

    private Integer discTuition;

    private Integer discTime;

    private String discDesc;

    private String discEffective;

    public Integer getDiscId() {
        return discId;
    }

    public void setDiscId(Integer discId) {
        this.discId = discId;
    }

    public String getDiscName() {
        return discName;
    }

    public void setDiscName(String discName) {
        this.discName = discName;
    }

    public Integer getDiscTuition() {
        return discTuition;
    }

    public void setDiscTuition(Integer discTuition) {
        this.discTuition = discTuition;
    }

    public Integer getDiscTime() {
        return discTime;
    }

    public void setDiscTime(Integer discTime) {
        this.discTime = discTime;
    }

    public String getDiscDesc() {
        return discDesc;
    }

    public void setDiscDesc(String discDesc) {
        this.discDesc = discDesc;
    }

    public String getDiscEffective() {
        return discEffective;
    }

    public void setDiscEffective(String discEffective) {
        this.discEffective = discEffective;
    }
}